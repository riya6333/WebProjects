    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            String pno1 = txtpno.getText();
            String start1 = txtsp.getSelectedItem().toString();
            String end1 = txtep.getSelectedItem().toString();
            String tno1 = txttno.getText();
            String tname1 = txttname.getText();
            String price1 = txttp.getText();
            SimpleDateFormat date_form = new SimpleDateFormat("yyyy-MM-dd");
            String date1 = date_form.format(txtdate.getDate());          
            String tickets1 = txtnt.getText();
            String total = txttotal.getText();
            
            
        try { 
            pst = con.prepareStatement("insert into reservation(Pno,Splace,Eplace,Tno,Tname,Price,Date,NofTicket,total)values(?,?,?,?,?,?,?,?,?)");
            pst.setString(1, pno1);
            pst.setString(2, start1);
            pst.setString(3, end1);
            pst.setString(4, tno1);
            pst.setString(5, tname1);
            pst.setString(6, price1);
            pst.setString(7, date1);
            pst.setString(8, tickets1);
            pst.setString(9, total);
            
            int k = pst.executeUpdate();
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "Record Addedd");
            }
            else{
                JOptionPane.showMessageDialog(this, "Record Failed");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


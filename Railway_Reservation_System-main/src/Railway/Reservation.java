    public void LoadStartPlace(){
        
        try {
            pst = con.prepareStatement("SELECT tstart FROM trains");
            rs = pst.executeQuery();
            txtsp.removeAllItems();
            
            while(rs.next())
            {
                txtsp.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    
    public void LoadEndPlace(){
        
        try {
            pst = con.prepareStatement("SELECT tend FROM trains");
            rs = pst.executeQuery();
            txtep.removeAllItems();
            
            while(rs.next())
            {
                txtep.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
    public void Load()
    {
        
            String splace = txtsp.getSelectedItem().toString();
            String eplace = txtep.getSelectedItem().toString();
        try {    
            pst = con.prepareStatement("select * from trains where tstart = ? and tend = ?");
            pst.setString(1, splace);
            pst.setString(2, eplace);
            rs = pst.executeQuery();
            
            if (rs.next() == true)
            {
                txttno.setText(rs.getString(1));
                txttname.setText(rs.getString(2));
                txttp.setText(rs.getString(5));
            }
            
            else{
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
